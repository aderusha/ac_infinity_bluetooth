package com.eternal.widget.guqiang;

public final class RangeSeekBarAdapter {
    public static void setMin(RangeSeekBar rangeSeekBar, int i) {
        rangeSeekBar.setMin(i);
    }

    public static void setDistance(RangeSeekBar rangeSeekBar, int i) {
        rangeSeekBar.setDistance(i);
    }

    public static void setUnit(RangeSeekBar rangeSeekBar, String str) {
        rangeSeekBar.setUnit(str);
    }

    public static void setLow(RangeSeekBar rangeSeekBar, int i) {
        rangeSeekBar.setLow(i);
    }

    public static void setHigh(RangeSeekBar rangeSeekBar, int i) {
        rangeSeekBar.setHigh(i);
    }

    public static void setHighClose(RangeSeekBar rangeSeekBar, boolean z) {
        rangeSeekBar.setHighSwitch(z);
    }

    public static void setLowClose(RangeSeekBar rangeSeekBar, boolean z) {
        rangeSeekBar.setLowSwitch(z);
    }
}
